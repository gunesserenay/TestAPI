            if (id == 0)
            {
                //_logger.LogError("Get Villa Error with Id" + id);
                return BadRequest();
            }

            var villa = await _dbVilla.GetAsync(x => x.Id == id);
            if (villa == null)
            {
                return NotFound();
            }
            return Ok(_mapper.Map<VillaDTO>(villa));
        }
